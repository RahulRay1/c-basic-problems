//Write a program in C++ to print the sum of two numbers using variables.

#include<iostream>
using namespace std;
int main(int argc, char const *argv[])
{
    int a, b, sum;
    a = 29;
    b = 30;
    sum = a+b;
    cout<<"The sum of these two numbers "<<a<<" and "<<b<<" is = "<<sum<<endl;

    return 0;
}
